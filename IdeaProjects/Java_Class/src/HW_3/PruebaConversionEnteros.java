package HW_3;

public class PruebaConversionEnteros {
    public static void main(String args[]) {
        System.out.printf("%d\n", 26);
        System.out.printf("%d\n", +26);
        System.out.printf("%d\n", -26);
        System.out.printf("%o\n", 26);
        System.out.printf("%x\n", 26);
        System.out.printf("%X\n", 26); // Added missing backslash
    }
}
